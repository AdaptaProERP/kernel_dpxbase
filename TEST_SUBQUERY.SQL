 SELECT  SUC_CODIGO,SUC_DESCRI,
 (SELECT COUNT(*) FROM DPCLIENTES WHERE CLI_CODSUC=SUC_CODIGO LIMIT 1)
  AS CLIENTES,
 (SELECT COUNT(*) FROM DPCTABANCO WHERE BCO_CODSUC=SUC_CODIGO LIMIT 1)
  AS BANCOS,SUC_EMPRES,SUC_ACTIVO 
 FROM DPSUCURSAL  
 WHERE (SELECT COUNT(*) FROM DPCTABANCO WHERE BCO_CODSUC=SUC_CODIGO LIMIT 1) =0  
 GROUP BY SUC_CODIGO  
 HAVING CLIENTES<2  
 ORDER BY SUC_CODIGO 
